  /// Asterisk device string for an extension. Prefers an explicit SIP username,
  /// else the dial number, over PJSIP.
  String _dialDevice(Extension ext) {
    final user = (ext.sipUsername != null && ext.sipUsername!.isNotEmpty)
        ? ext.sipUsername!
        : ext.number;
    return 'PJSIP/${_sanitize(user)}';
  }

  /// VoiceMail() mailbox token. Prefers the explicit mailbox number, else the
  /// box id, in the `default` context.
  String _voicemailMailbox(VoicemailBox vm) {
    final box = (vm.mailboxNumber != null && vm.mailboxNumber!.isNotEmpty)
        ? vm.mailboxNumber!
        : vm.id;
    return '${_sanitize(box)}@default';
  }

  // ── Time helpers ────────────────────────────────────────────────────

  /// minutes-from-midnight → `HH:MM` for GotoIfTime.
  String _hhmm(int minute) {
    final h = (minute ~/ 60).clamp(0, 23);
    final m = (minute % 60).clamp(0, 59);
    final hh = h.toString().padLeft(2, '0');
    final mm = m.toString().padLeft(2, '0');
    return '$hh:$mm';
  }

  /// Our days are 1=Mon..7=Sun. Asterisk GotoIfTime uses mon,tue,...,sun and
  /// accepts ranges; we emit a comma list of the three-letter day names.
  String _weekdays(List<int> days) {
    const names = {
      1: 'mon',
      2: 'tue',
      3: 'wed',
      4: 'thu',
      5: 'fri',
      6: 'sat',
      7: 'sun',
    };
    final out = days.map((d) => names[d]).whereType<String>().toList();
    return out.isEmpty ? '*' : out.join('&');
  }

  /// Asterisk month token (jan..dec) from a 1..12 month number.
  String? _monthName(int? m) {
    const names = [
      'jan', 'feb', 'mar', 'apr', 'may', 'jun',
      'jul', 'aug', 'sep', 'oct', 'nov', 'dec',
    ];
    if (m == null || m < 1 || m > 12) return null;
    return names[m - 1];
  }

  // ── Caveats ─────────────────────────────────────────────────────────

  @override
  List<String> notes(CallSystemProject project) {
    final out = <String>[
      'Queues reference a queues.conf object by name; you must define each '
          'queue (members, strategy, music-on-hold) in queues.conf — only the '
          'dialplan entry point is generated here.',
      'Voicemail boxes reference mailboxes in the "default" context of '
          'voicemail.conf; provision the mailboxes and greetings there.',
      'SIP/PJSIP device names are derived from each extension\'s sipUsername '
          '(or dial number); confirm they match your pjsip.conf endpoints.',
      'Inbound DID patterns in [$_inboundContext] use the raw E.164 digits; '
          'adjust to match what your trunk delivers (some strip the leading +).',
    ];

    // Ring strategies other than ringAll are approximated as a parallel Dial.
    final nonRingAll = project.ringGroups
        .where((r) => r.strategy != RingStrategy.ringAll)
        .map((r) => r.name)
        .toList();
    if (nonRingAll.isNotEmpty) {
      out.add('Ring groups ${nonRingAll.join(', ')} use a sequential/rotating '
          'strategy (${'hunt/roundRobin/etc.'}); Asterisk dialplan rings them '
          'in parallel here. Use a hunt-group construct (e.g. dialplan loops or '
          'FreePBX ring groups) for true sequential behaviour.');
    }

    // Detect node types Asterisk can't natively express across all flows.
    final hasSpeech = _anyNode(project, CallNodeType.gatherSpeech);
    final hasAi = _anyNode(project, CallNodeType.aiVoicebot);
    final hasHttp = _anyNode(project, CallNodeType.httpRequest);
    final hasSubFlow = _anyNode(project, CallNodeType.subFlow);

    if (hasSpeech) {
      out.add('gatherSpeech nodes need an external speech-recognition engine '
          '(res_speech/AGI); the generated dialplan only stubs them.');
    }
    if (hasAi) {
      out.add('aiVoicebot nodes run in the Nexus managed runtime (streamed '
          'STT/LLM/TTS); the dialplan emits a placeholder — bridge via AGI/'
          'external media to enable them on raw Asterisk.');
    }
    if (hasHttp) {
      out.add('httpRequest nodes use CURL() for a best-effort GET; POST bodies, '
          'headers and JSON parsing require AGI or func_curl configuration.');
    }
    if (hasSubFlow) {
      out.add('subFlow nodes Goto the sub-flow context without a true return; '
          'for call/return semantics convert them to Gosub()/Return() manually.');
    }

    if (project.isOutbound) {
      out.add('This is an outbound project: configure an outbound trunk and '
          'CallerID, and ensure TCPA/consent/DNC compliance before dialing.');
    }

    return out;
  }

  bool _anyNode(CallSystemProject project, CallNodeType type) {
    for (final flow in project.flows) {
      for (final node in flow.nodes) {
        if (node.type == type) return true;
      }
    }
    return false;
  }
