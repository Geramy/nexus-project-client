/// Seeds initial data and guarantees the built-in "Default" client always
/// exists. With integer auto-increment PKs we let the DB assign ids; on a fresh
/// database the Default client becomes client_pk = 1.
///
/// We intentionally do NOT seed a sample project or tasks: the first-run
/// onboarding wizard walks the user through creating their real first project
/// (and picking an agent pack), so a fresh install starts clean instead of with
/// a confusing placeholder project.
Future<void> seedInitialData(NexusDatabase db) async {
  // Configurable setup-interview flows are global + idempotent — seed them
  // every launch (independent of the Default-client check below) so new built-in
  // flows (e.g. new IVR sub-categories) appear without a DB reset.
  try {
    await db.seedSetupFlows();
  } catch (e) {
    debugPrint('Seeder: setup flows warning (non-fatal): $e');
  }

  // Local training-data tables (Account → Export Tracking). Plain SQLite via raw
  // SQL, created idempotently so no schema migration / codegen is needed.
  try {
    await db.ensureTrainingTables();
  } catch (e) {
    debugPrint('Seeder: training tables warning (non-fatal): $e');
  }

  // Adaptive scoped-vocabulary tree (industries → sub-axes → scoped
  // objectives/features/libraries + platform stacks), bundled as an asset and
  // generated by deep research. Idempotent per-industry; safe every launch.
  try {
    await _seedScopedVocab(db);
  } catch (e) {
    debugPrint('Seeder: scoped vocab warning (non-fatal): $e');
  }

  await _maybeCreateDefaults(db);

  // Repair tool permissions on already-seeded personas. The Project Manager and
  // Coordinator must always be able to author user stories, but older seeds (and
  // installs on other machines) baked a default-deny map that blocked the story
  // tools because no skill bundle included them. Idempotent: only writes when a
  // story tool isn't already granted.
  try {
    await _grantStoryToolsToManagers(db);
  } catch (e) {
    debugPrint('Seeder: story-permission reconcile warning (non-fatal): $e');
  }

  // Repair the Project Manager / Coordinator default collections (PM → Interview,
  // Coordinator → Discovery) on installs seeded before per-role collections.
  try {
    await _setManagerDefaultCollections(db);
  } catch (e) {
    debugPrint('Seeder: collection-default reconcile warning (non-fatal): $e');
  }

  // Migrate personas off any RETIRED/renamed Omni collection (e.g. a collection
  // the server replaced) to their role-appropriate default, so existing projects
  // stop sending a model id the router no longer serves.
  try {
    await _migrateRetiredOmniCollections(db);
  } catch (e) {
    debugPrint('Seeder: retired-collection migration warning (non-fatal): $e');
  }

  // Ensure the Coordinator can FINALIZE a merge (approve a clean one / reject a
  // conflicted one for rework). Without these the orchestrator's merge stage
  // loops every conflicting task to the cap and Blocks it. Repairs personas
  // seeded before the `merge-integration` skill existed.
  try {
    await _ensureCoordinatorCanFinalizeMerges(db);
  } catch (e) {
    debugPrint('Seeder: merge-permission reconcile warning (non-fatal): $e');
  }

  // Lean down the Generalist (the coder) to its coder-only tool set on installs
  // seeded before the trim, so its per-turn tool payload shrinks (the Generalist
  // determines most of the project's speed/token cost). Generalist-only.
  try {
    await _leanGeneralistTools(db);
  } catch (e) {
    debugPrint('Seeder: generalist-tool reconcile warning (non-fatal): $e');
  }
}

/// Forces the coder-irrelevant tools ([kGeneralistDeniedTools]) to `deny` on
/// existing Generalist personas, shrinking the tool payload the coder carries
/// every turn. GENERALIST-ONLY (agent permissions are unique per agent) and
/// idempotent — writes only when some denied tool isn't already denied, and
/// preserves every other override (so a deliberately-tuned coder tool is kept).
Future<void> _leanGeneralistTools(NexusDatabase db) async {
  for (final p in await db.getAllAgentPersonas()) {
    if (agentRoleFromKey(p.title) != AgentRole.sdeGeneralist) continue;
    final perms = AgentToolPermissions.fromConfigJson(p.configJson);
    final needs = kGeneralistDeniedTools.any(
      (t) => perms.permFor(t) != ToolPerm.deny,
    );
    if (!needs) continue;

    final merged = Map<String, ToolPerm>.from(perms.overrides);
    for (final t in kGeneralistDeniedTools) {
      merged[t] = ToolPerm.deny;
    }
    final newJson = AgentToolPermissions.writeIntoConfigJson(
      p.configJson,
      merged,
    );
    await db.updateAgentPersona(
      p.toCompanion(true).copyWith(configJson: Value(newJson)),
    );
  }
}

/// Grants the Coordinator the two tools its merge-integration duty requires —
/// `approve_task` (ask; the autonomous merge stage auto-approves) and
/// `reject_task` (grant) — on existing Coordinator personas that still deny them
/// (the seed gap that left conflicting tasks unable to drain). Coordinator-only;
/// idempotent (writes only when a tool is currently denied/absent).
Future<void> _ensureCoordinatorCanFinalizeMerges(NexusDatabase db) async {
  for (final p in await db.getAllAgentPersonas()) {
    if (agentRoleFromKey(p.title) != AgentRole.coordinator) continue;
    final perms = AgentToolPermissions.fromConfigJson(p.configJson);
    final approveOk = perms.permFor('approve_task') != ToolPerm.deny;
    final rejectOk = perms.permFor('reject_task') != ToolPerm.deny;
    if (approveOk && rejectOk) continue;

    final merged = Map<String, ToolPerm>.from(perms.overrides)
      ..['approve_task'] = ToolPerm.ask
      ..['reject_task'] = ToolPerm.grant;
    final newJson = AgentToolPermissions.writeIntoConfigJson(
      p.configJson,
      merged,
    );
    await db.updateAgentPersona(
      p.toCompanion(true).copyWith(configJson: Value(newJson)),
    );
  }
}

/// Repairs existing Project Manager / Coordinator personas to their purpose-built
/// default Omni collections (PM → Interview, Coordinator → Discovery), for
/// installs seeded before per-role collections existed. Idempotent and
/// NON-DESTRUCTIVE: only updates a persona still on the generic product default
/// (or with no collection set) — never overwrites a deliberately chosen one.
Future<void> _setManagerDefaultCollections(NexusDatabase db) async {
  for (final p in await db.getAllAgentPersonas()) {
    final role = agentRoleFromKey(p.title);
    if (role != AgentRole.projectManager && role != AgentRole.coordinator) {
      continue;
    }
    final want = defaultOmniCollectionForTitle(p.title);
    final cur = p.omniCollectionModel?.trim() ?? '';
    if (cur == want) continue;
    // Respect a deliberately chosen collection; only set when unset or still on
    // the old generic default.
    if (cur.isNotEmpty && cur != kDefaultOmniCollection) continue;
    await db.updateAgentPersona(
      p.toCompanion(true).copyWith(omniCollectionModel: Value(want)),
    );
  }
}

/// Migrate every persona still pointing at a RETIRED Omni collection (one the
/// server renamed/removed — see [kRetiredOmniCollections]) to its role-appropriate
/// default ([defaultOmniCollectionForTitle]). Idempotent: a persona already on a
/// live collection is untouched. Without this, existing projects keep sending the
/// old collection id and the router can't serve it.
Future<void> _migrateRetiredOmniCollections(NexusDatabase db) async {
  final retired = kRetiredOmniCollections.toSet();
  if (retired.isEmpty) return;
  for (final p in await db.getAllAgentPersonas()) {
    final cur = p.omniCollectionModel?.trim() ?? '';
    if (cur.isEmpty || !retired.contains(cur)) continue;
    final want = defaultOmniCollectionForTitle(p.title);
    await db.updateAgentPersona(
      p.toCompanion(true).copyWith(omniCollectionModel: Value(want)),
    );
    debugPrint(
      'Seeder: migrated persona "${p.name}" off retired collection "$cur" → "$want".',
    );
  }
}

/// Ensures every Project Manager / Coordinator persona grants the full set of
/// user-story tools, repairing personas seeded before `story-authoring` existed
/// (their stored configJson explicitly denies these tools).
Future<void> _grantStoryToolsToManagers(NexusDatabase db) async {
  // Source of truth for the story tools — kept in sync with the catalog instead
  // of a hand-copied list.
  final storyTools = [
    for (final s in kCoordinatorToolSpecs)
      if (s.category == 'Stories') s.name,
  ];
  if (storyTools.isEmpty) return;

  for (final p in await db.getAllAgentPersonas()) {
    final role = agentRoleFromKey(p.title);
    if (role != AgentRole.projectManager && role != AgentRole.coordinator) {
      continue;
    }
    final perms = AgentToolPermissions.fromConfigJson(p.configJson);
    final needs = storyTools.any((t) => perms.permFor(t) != ToolPerm.grant);
    if (!needs) continue;

    // Preserve every existing override; only force the story tools to grant.
    final merged = Map<String, ToolPerm>.from(perms.overrides);
    for (final t in storyTools) {
      merged[t] = ToolPerm.grant;
    }
    final newJson = AgentToolPermissions.writeIntoConfigJson(
      p.configJson,
      merged,
    );
    await db.updateAgentPersona(
      p.toCompanion(true).copyWith(configJson: Value(newJson)),
    );
  }
}

/// Bundled scoped-vocabulary catalog version. BUMP THIS whenever
/// `assets/setup/scoped_vocab.json` is regenerated so existing installs reconcile
/// (insert new industries, update changed ones) on next launch.
const int _scopedVocabVersion = 1;

/// Load the bundled scoped-vocabulary catalog and reconcile it into the DB.
/// Version-gated: if the stored catalog version already matches the bundled one,
/// this returns immediately WITHOUT loading/parsing the (large) asset — so an
/// unchanged launch does no work. On first seed (version null) or a bumped
/// catalog, it inserts new records and updates existing ones. Empty/missing
/// asset is a no-op (the interview falls back to generic flow suggestions).
Future<void> _seedScopedVocab(NexusDatabase db) async {
  if (await db.scopedCatalogVersion() == _scopedVocabVersion) return;
  String raw;
  try {
    raw = await rootBundle.loadString('assets/setup/scoped_vocab.json');
  } catch (_) {
    return; // asset not bundled
  }
  if (raw.trim().isEmpty) return;
  final decoded = jsonDecode(raw);
  if (decoded is! List || decoded.isEmpty) return;
  await db.seedSetupScopes(decoded, version: _scopedVocabVersion);
}

Future<void> _maybeCreateDefaults(NexusDatabase db) async {
  final existingDefault = await db.getDefaultClient();
  if (existingDefault != null) return; // Already have a Default client.

  try {
    // Seeds the Default client + a Local Lemonade server + skill prefabs + the
    // default agent pack (Application Development).
    await db.createClientWithDefaults(name: 'Default', isDefault: true);
  } catch (e) {
    debugPrint('Seeder: could not ensure Default client (non-fatal): $e');
  }
}
